
# String은 문자열, ""로 감싸면 문자로 인식, "123" + "345" = "123345"
# [] 대괄호 안에 인덱스를 넣어 문자열 지정 가능
print("Hello"[4])
print("123" + "345")

# Integer는 정수, 숫자는 숫자로 인식, 123 + 345 = 468

print(123 + 345)

# Float 부동 소수점을 가진 숫자

print(3.14159)

# Boolean은 참과 거짓, True 혹은 False를 반환

print(1 == 1) # True
print(1 == 2) # False